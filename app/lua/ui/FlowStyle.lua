function updateFlowColor()
	luce.FlowStyle.setTitleBackgroundColor(ui.color.themeColorB3)
	luce.FlowStyle.setTitleHighlightColor(ui.color.themeColorB4)
	luce.FlowStyle.setTitleBorderColor(ui.color.themeColorA2)
	luce.FlowStyle.setTitleSplitColor(ui.color.themeColorD4)
	luce.FlowStyle.setTitleTextColor(ui.color.themeColorD2)
	luce.FlowStyle.setTitleTextHighlightColor(ui.color.themeColorD1)

	luce.FlowStyle.setResizerColor(ui.color.themeColorA1)

	luce.FlowStyle.setContainerBorderColor(ui.color.themeColorB1)

	luce.FlowStyle.setButtonIconColor(ui.color.themeColorD2)
	luce.FlowStyle.setButtonIconBackgroundColor(ui.color.themeColorD4)
	luce.FlowStyle.setAdsorbColor(ui.color.themeColorE3)
end